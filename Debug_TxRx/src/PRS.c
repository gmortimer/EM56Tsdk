u32 *PRSymbPtr( u32 NFFT )
{
	switch ( NFFT ) {
	    case 2048: return( PRS_2048 );
	    case 1024: return( PRS_1024 );
	    case  512: return( PRS_512  );
	    case  256: return( PRS_256  );
	    default:   return( NULL );
	}
}

u32 *CopyPRSymb( u32 NFFT, u32 *pBuf, double sf )
{
	u32 *pPRS = PRSymbPtr( NFFT );
	for ( int i = 0; i < NFFT; i++ ) {
		Cplx32 z = U32toCplx32( *pPRS++ );
		*pBuf++ = Cplx32toU32( ScaleCplx32( z, sf ) );
//        *pBuf++ = 0;
	}
	return( pBuf );
}

